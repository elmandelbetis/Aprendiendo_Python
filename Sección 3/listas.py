# Creación de una lista
student_grades = [9.1, 8.8, 7.5]

# Creación de listas dentro de otras listas
lista_2 = [9, "Hello", [1, 2, 4.33]]

print(student_grades)
print(lista_2)

# Operaciones con listas
print(lista_2 * 3)  # Imprime la lista 3 veces

# Concatenación de listas
print(student_grades + lista_2)